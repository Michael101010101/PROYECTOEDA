    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int tiempoActual = 0;
        int max = colaProcesos.contarNodos();
        int quantum = colaProcesos.getQuantum();
        String Log = "";
        
        while(!colaProcesos.estaVacia()){
            NodoProceso procesoActual = colaProcesos.desencolarProceso();
            
            if(!procesoActual.isEjecutado()){
                if(procesoActual.getTiempoLlegada()>tiempoActual){
                    Log+="Tiempo "+procesoActual.getTiempoLlegada()+": Llega proceso "+procesoActual.getNombre()+"\n";
                    tiempoActual = procesoActual.getTiempoLlegada();
                }
                procesoActual.setTiempoInicio(tiempoActual);
                Log+="Tiempo "+tiempoActual+": "+procesoActual.getNombre()+" entra a ejecución\n";
            }
            
            if(procesoActual.getTiempoRest() > quantum){
                // El proceso no ha terminado y excede el quantum
                procesoActual.setEjecutado(true);
                tiempoActual += quantum;
                Log+=colaProcesos.tiemposLlegada(tiempoActual);
                procesoActual.ActualizarTiempoRest(quantum);
                colaProcesos.encolarProceso(procesoActual); // Vuelve a la cola de listos
                Log+="Tiempo " + tiempoActual + ": " + procesoActual.getNombre() + " se conmuta. Pendiente por ejecutar "+procesoActual.getTiempoRest()+"ms\n";
            } else {
                // El proceso termina su ejecución
                procesoActual.setEjecutado(true);
                tiempoActual += procesoActual.getTiempoRest();
                Log+=colaProcesos.tiemposLlegada(tiempoActual);
                procesoActual.ActualizarTiempoRest(procesoActual.getTiempoRest());
                procesoActual.setTiempoFin(tiempoActual);
                refGN.insertar(procesoActual);
                Log+="Tiempo " + tiempoActual + ": " + procesoActual.getNombre() + " termina su ejecución\n";
            }   
        }
        Log+="Tiempo " + tiempoActual + ": Se ejecutaron todos los procesos";
        this.jTextArea1.setText(Log);
        CargarTabla();
    }//GEN-LAST:event_jButton3ActionPerformed

