    public void CargarTabla(){
        refGN.ordenarAscendente();
        int filas = this.jTable1.getRowCount();
        for(int i=0;i<filas;i++)
        {
            modelo.removeRow(0);
        }
        
        String datos[] = new String[5];
        NodoProceso ptr = refGN.root;
        
        while(ptr!=null)
        {
            datos[0] = ptr.getNombre();
            datos[1] = String.valueOf(ptr.getTiempoEjec());
            datos[2] = String.valueOf(ptr.getTiempoLlegada());
            datos[3] = String.valueOf(ptr.getTiempoInicio());
            datos[4] = String.valueOf(ptr.getTiempoFin());
            ptr = ptr.getSiguiente();
            modelo.addRow(datos);
        }
    }
